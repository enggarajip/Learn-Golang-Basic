package main

import "fmt"

func main() {
	var result = 18 + 18
	fmt.Println(result)

	var a = 20
	var b = 30
	var c = a * b
	fmt.Println(c)

	var i = 20
	i += 30 // i = +30
	fmt.Println(i)

	i++ //i = i + 1
	fmt.Println(i)

	var negative = -200
	var positive = +200
	fmt.Println(negative)
	fmt.Println(positive)
}
